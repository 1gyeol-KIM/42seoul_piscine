#include <unistd.h>

void	ft_print_num(int a)
{
	char n[2];

	n[0] = a / 10 + 48;
	n[1] = a % 10 + 48;
	write(1, &n, 2);
}

void	ft_print_comb2(void)
{
	int		n[2];
	char	c[2];

	c[0] = ',';
	c[1] = ' ';
	n[0] = 0;
	while (n[0] <= 98)
	{
		n[1] = n[0] + 1;
		while (n[1] <= 99)
		{
			ft_print_num(n[0]);
			write(1, &c[1], 1);
			ft_print_num(n[1]);
			if (!(n[0] == 98 && n[1] == 99))
			{
				write(1, &c, 2);
			}
			n[1]++;
		}
		n[0]++;
	}
}
