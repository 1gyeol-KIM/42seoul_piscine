#include <unistd.h>

int		main(int argc, char *argv[])
{
	int		i;
	int		j;

	i = 1;
	j = 0;
	while (i < argc)
	{
		while (argv[i][j])
			j++;
		write(1, &argv[i][0], j);
		write(1, "\n", 1);
		j = 0;
		i++;
	}
	return (0);
}
