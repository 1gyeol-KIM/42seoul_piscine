#include <unistd.h>

int		main(int argc, char *argv[])
{
	int		i;

	i = 0;
	while (argc > 1)
	{
		while (argv[argc - 1][i])
			i++;
		write(1, &argv[argc - 1][0], i);
		write(1, "\n", 1);
		argc--;
		i = 0;
	}
	return (0);
}
