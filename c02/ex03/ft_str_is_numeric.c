int		ft_str_is_numeric(char *str)
{
	int		i;
	char	c;

	i = 0;
	if (str[0] == '\0')
		return (1);
	while (!(str[i] == '\0'))
	{
		c = str[i];
		if (!((0 <= (c - '0')) && ((c - '0') <= 9)))
			return (0);
		i++;
	}
	return (1);
}
