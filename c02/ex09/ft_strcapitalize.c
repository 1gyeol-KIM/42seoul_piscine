char	*ft_strcapitalize(char *str)
{
	int 	i;
	char	c;

	i = 0;
	while (!(str[i] == '\0'))
	{
		c = str[i];
		if ('a' <= c <= 'z' || 'A' <= c <= 'Z' || '0' <= c <= '9')
		{
			if ('a' <= c <= 'z')
				str[i] -= 32;
			i++;
			c = str[i];
			while ( 'a' <= c <= 'z' || 'A' <= c <= 'Z' || '0' <= c <= '9')
			{
				if ('A' <= c <= 'Z')
					str[i] += 32;
				i++;
			}
			continue;
		}
		i++;
	}
	return (str);
}
